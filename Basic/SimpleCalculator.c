/*
File Name: SimpleCalculator.c
Purpose: Perform addition, subtraction, multiplication, and division.
*/

#include <stdio.h>

int main() {
    int a = 8, b = 4;
    printf("Addition: %d\n", a + b);
    printf("Subtraction: %d\n", a - b);
    printf("Multiplication: %d\n", a * b);
    printf("Division: %d\n", a / b);
    return 0;
}
